class TextSanitizer {
  /// チーム名や道場名のゆれ（全角半角・スペース）を完全に統一する最強のクリーナー関数
  static String clean(String input) {
    if (input.isEmpty) return '';

    String text = input;

    // 1. 全角の英数字を半角に変換（ＡＢＣ１２３ ➔ ABC123）
    const fullToHalf = {
      '０':'0', '１':'1', '２':'2', '３':'3', '４':'4',
      '５':'5', '６':'6', '７':'7', '８':'8', '９':'9',
      'Ａ':'A', 'Ｂ':'B', 'Ｃ':'C', 'Ｄ':'D', 'Ｅ':'E',
      'Ｆ':'F', 'Ｇ':'G', 'Ｈ':'H', 'Ｉ':'I', 'Ｊ':'J',
      'Ｋ':'K', 'Ｌ':'L', 'Ｍ':'M', 'Ｎ':'N', 'Ｏ':'O',
      'Ｐ':'P', 'Ｑ':'Q', 'Ｒ':'R', 'Ｓ':'S', 'Ｔ':'T',
      'Ｕ':'U', 'Ｖ':'V', 'Ｗ':'W', 'Ｘ':'X', 'Ｙ':'Y', 'Ｚ':'Z',
      'ａ':'a', 'ｂ':'b', 'ｃ':'c', 'ｄ':'d', 'ｅ':'e',
      'ｆ':'f', 'ｇ':'g', 'ｈ':'h', 'ｉ':'i', 'ｊ':'j',
      'ｋ':'k', 'ｌ':'l', 'ｍ':'m', 'ｎ':'n', 'ｏ':'o',
      'ｐ':'p', 'ｑ':'q', 'ｒ':'r', 'ｓ':'s', 'ｔ':'t',
      'ｕ':'u', 'ｖ':'v', 'ｗ':'w', 'ｘ':'x', 'ｙ':'y', 'ｚ':'z',
    };
    fullToHalf.forEach((k, v) => text = text.replaceAll(k, v));

    // 2. 全角スペースを半角スペースに変換
    text = text.replaceAll('　', ' ');

    // 3. 【重要】文字列の前、後ろ、そして「間」にあるスペースをすべて消し去る
    text = text.replaceAll(' ', '');

    // 4. 見えない改行コードなどをトリムして綺麗な状態にする
    return text.trim();
  }
}
